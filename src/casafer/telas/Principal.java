    public class PintarTabela extends DefaultTableCellRenderer {

        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            JLabel label = (JLabel) super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column); //To change body of generated methods, choose Tools | Templates.
            
            //INICIA AS CORES***************************************************
            Color background = Color.WHITE;
            Color foreground = Color.BLACK;
            
            if(isSelected){
                background = CINZA;
                foreground = Color.WHITE;
            }
            

            //PINTA DESCONTOS***************************************************
            Object objeto = table.getValueAt(row, 7);
            Double valor = Double.parseDouble(objeto.toString());
            
            
            if(valor > 8.00){
                background = AMARELO;
               foreground = Color.WHITE;
            }
            
            
            //PINTA Devolucoes***************************************************
            objeto = table.getValueAt(row, 9);
            int cfop = Integer.parseInt(objeto.toString());
            
            
            if(cfop == 1202){
                background = VERMELHO_ES;
               foreground = Color.WHITE;
            }
            //PINTA NOTAS CANCELADAS********************************************
            
            objeto = table.getValueAt(row, 8);
            String letra = objeto.toString();
            
            if(letra.equals("C")){
               background = VERMELHO;
               foreground = Color.WHITE;
            }
            
            label.setBackground(background);
            label.setForeground(foreground);
        return label;
        }
    }
