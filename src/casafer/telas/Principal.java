    private void botaoAtualizar() {
        String temp1 = jTextFieldDataInicio.getText();
        String temp2 = jTextFieldDataFim.getText();
        String temp3 = jComboBoxVendedores.getSelectedItem().toString();
        
        String dataInicio = "";
        String dataFim = "";
        String codVendedor = "";
        
        dataInicio = dataInicio + temp1.substring(6, 10);
        dataInicio = dataInicio + temp1.substring(3, 5);
        dataInicio = dataInicio + temp1.substring(0, 2);
        dataFim = dataFim + temp2.substring(6, 10);
        dataFim = dataFim + temp2.substring(3, 5);
        dataFim = dataFim + temp2.substring(0, 2);

        codVendedor = temp3.substring(0, 2);
        
        
        jLabelDataAtualizacao.setText(getDateTime());
        
        DefaultTableModel model = (DefaultTableModel) jTableNotas.getModel();
        while (jTableNotas.getRowCount() > 0) {
            model.removeRow(0);
        }
        
        ArrayList<NotasVendas> listaNotas = new ArrayList();
        DaoNotasVendas daoNotasVendas = new DaoNotasVendas();
        //se for maior que zero foi selecionado um vendedor        //se for maior que zero foi selecionado um vendedor
        
        if(jComboBoxVendedores.getSelectedIndex() > 0){
            //com vendedor selecionado
            try {
            listaNotas = daoNotasVendas.todasNotasPorVendedor(dataInicio,dataFim,codVendedor);
            } catch (ClassNotFoundException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            //todos os vendedores
            try {
                listaNotas = daoNotasVendas.todasNotas(dataInicio,dataFim);
            } catch (ClassNotFoundException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        double totalLiquido = 0;
        double totalBruto = 0;
        double descontos = 0;
        double devolucoes = 0;
        double canceladas = 0;
        for(NotasVendas nota: listaNotas){
            
           model.addRow(new Object[]{nota.getSer_Codigo(), nota.getNF_Numero(), nota.getNF_DataSaida(), nota.getNF_Vendedores(), nota.getNF_VlrMercadorias(),nota.getNF_VlrNota(),nota.getNF_VlrDesc(),nota.getNF_PercDesc(),nota.getNF_Situcao(),nota.getOpFis_Codigo()});
           String tempCFOP = Integer.toString(nota.getOpFis_Codigo());
            String tempDIGITO = tempCFOP.substring(1, 2);
            int digito = Integer.parseInt(tempDIGITO); 
           if(nota.getNF_Situcao().equals("A") && digito == 1 || digito == 4){
                totalLiquido = totalLiquido + nota.getNF_VlrNota();
                totalBruto = totalBruto + nota.getNF_VlrMercadorias();
                descontos = descontos + nota.getNF_VlrDesc();
            }
            if(nota.getNF_Situcao().equals("C")){
                canceladas = canceladas + nota.getNF_VlrNota();
            }
            
            if(digito == 2 && nota.getNF_Situcao().equals("A")){
                devolucoes = devolucoes + nota.getNF_VlrNota();
            }
        }
        double totalLiquidoMenosDev = totalLiquido - devolucoes;
        //mostra totais nos campos
        //jLabelTotalLiquido.setText(formtarDecimal(totalLiquido));
        jLabelTotalLiquido.setText(formtarDecimal(totalLiquidoMenosDev));
        jLabelTotalBruto.setText(formtarDecimal(totalBruto));
        jLabelTotalDescontos.setText("(-) " + formtarDecimal(descontos));
        jLabelTotalCancelamentos.setText(formtarDecimal(canceladas));
        jLabelTotalDevolucoes.setText("(-) " +formtarDecimal(devolucoes));
        
        //CHAMADA DA PINTURA DA TABELA
        jTableNotas.setDefaultRenderer(Object.class, new PintarTabela());

    }
